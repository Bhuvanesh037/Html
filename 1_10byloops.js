//1 to 10 by using for loop
console.log("1 to 10 by using for loop")
for(let i=1;i<=10;i++){
    console.log(i);
}

////1 to 10 by using while loop
console.log("1 to 10 by using while loop")
let i=1;
while(i<=10){
    console.log(i);
    i++;
}
